package com.tka.dec17;

public class LogicalProgramming {
	void addMethod(int n1,int n2) {
		System.out.println("Addition:"+(n1+n2));
		}
	void subMethod(int n1,int n2) {
		System.out.println("Subtraction:"+(n1-n2));
		}
	void divMethod(int n1,int n2) {
		System.out.println("Division:"+(n1/n2));
		}
	void moduloMethod(int n1,int n2) {
		System.out.println("Modulo:"+(n1%n2));
		}
	void GreatnumMethod(int n1,int n2) {
		System.out.println("Greater:"+(n1>n2));
		}
	void LessnumMethod(int n1,int n2) {
		System.out.println("Lessthan:"+(n1<n2));
		}
	void equalMethod(int n1,int n2) {
		System.out.println("Equal:"+(n1==n2));
		}
	
	
}
